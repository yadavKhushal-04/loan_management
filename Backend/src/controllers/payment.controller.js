const addPayment = async (req, res) => {
    try {
        const { loanId } = req.params
        const { amount, paidDate, monthFor, method, note } = req.body

        const loan = await Loan.findById(loanId)
        if (!loan) {
            return res.status(404).json({ success: false, message: `Loan not found` })
        }

        const existing = await Payment.findOne({ loanId, monthFor })
        if (existing) {
            return res.status(400).json({
                success: false,
                message: `Payment for ${monthFor} has already been recorded`
            })
        }

        // build ordered list of every month in the loan
        const start = new Date(loan.startDate)
        const allMonths = []
        for (let i = 0; i < loan.durationMonths; i++) {
            const d = new Date(start.getFullYear(), start.getMonth() + i, 1)
            allMonths.push(`${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`)
        }

        const startIdx = allMonths.indexOf(monthFor)
        if (startIdx === -1) {
            return res.status(400).json({
                success: false,
                message: `monthFor is outside this loan's duration`
            })
        }

        // cascade the paid amount across months, capped at EMI each
        let remaining = amount
        let idx = startIdx
        const createdPayments = []

        while (remaining > 0 && idx < allMonths.length) {
            const currentMonthKey = allMonths[idx]

            const alreadyPaid = await Payment.findOne({ loanId, monthFor: currentMonthKey })
            if (alreadyPaid) {
                idx++
                continue
            }

            const allocated = Math.min(remaining, loan.emiAmount)

            const payment = new Payment({
                loanId,
                amount: allocated,
                paidDate: paidDate || new Date(),
                monthFor: currentMonthKey,
                method,
                note: currentMonthKey === monthFor ? note : `Auto-allocated from overpayment for ${monthFor}`,
                autoGenerated: currentMonthKey !== monthFor
            })
            await payment.save()

            loan.payments.push(payment._id)
            createdPayments.push(payment)

            remaining -= allocated
            idx++
        }

        await loan.save()

        const autoPaidMonths = createdPayments
            .filter(p => p.autoGenerated)
            .map(p => p.monthFor)

        res.status(201).json({
            success: true,
            message: autoPaidMonths.length > 0
                ? `Payment recorded. Also covered: ${autoPaidMonths.join(', ')}`
                : 'Payment recorded successfully',
            payments: createdPayments
        })
    }
    catch (err) {
        res.status(500).json({
            success: false,
            message: `Failed to record payment: ${err.message}`
        })
    }
}


const deletePayment = async (req, res) => {
    try {
        const { paymentId } = req.params

        const payment = await Payment.findById(paymentId)
        if (!payment) {
            return res.status(404).json({
                success: false,
                message: `Payment not found`
            })
        }

        // remove payment reference from the loan
        await Loan.findByIdAndUpdate(
            payment.loanId,
            { $pull: { payments: payment._id } }
        )

        await Payment.findByIdAndDelete(paymentId)

        res.status(200).json({
            success: true,
            message: `Payment deleted successfully`
        })
    }
    catch (err) {
        res.status(500).json({
            success: false,
            message: `Failed to delete payment: ${err.message}`
        })
    }
}


const getPaymentsByLoan = async (req, res) => {
    try {
        const { loanId } = req.params
        const { page, limit } = req.query

        const { pageNum, limitNum, skip, getMeta } = paginate({}, page, limit)

        const [payments, total] = await Promise.all([
            Payment.find({ loanId }).sort({ paidDate: 1 }).skip(skip).limit(limitNum),
            Payment.countDocuments({ loanId })
        ])

        res.status(200).json({
            success: true,
            meta: getMeta(total),
            payments
        })
    }
    catch (err) {
        res.status(500).json({
            success: false,
            message: `Failed to get payment details for this loan: ${err.message}`
        })
    }
}


